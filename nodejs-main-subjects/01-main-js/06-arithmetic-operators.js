var number = 6;
console.log(number+"4"); // It will return 64
console.log(number+4); // It will return 10
console.log(number-4); // It will return 2
console.log(number*4); // It will return 24
console.log(number/4); // It will return 1,5
console.log(number%4); // It will return 2
console.log(++number); // It will return 7
console.log(number++); // It will return 7
console.log(number); // It will return 8
console.log(--number); // It will return 7
console.log(number--); // It will return 7
console.log(number); // It will return 6

